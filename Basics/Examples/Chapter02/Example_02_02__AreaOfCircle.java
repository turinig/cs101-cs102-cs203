package Basics.Examples.Chapter02;

// Example 2.2 from "Java Illuminated (5th Ed.)", chapter 2, page 43.
public class Example_02_02__AreaOfCircle {
	
	public static void main( String[] args ) {
		final double PI = 3.14159; // Define the constant storing the value of PI.
      double radius = 3.5; // Define a variable storing the value of the circle radius.
      double area = PI * radius * radius; // Calculate circle area and store the result in a variable.
      System.out.println("The area of the circle is: " + area); // Output the result on the console.
	}
		
}


