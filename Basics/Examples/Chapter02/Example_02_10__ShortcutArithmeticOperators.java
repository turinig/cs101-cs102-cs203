package Basics.Examples.Chapter02;

// Example 2.10 from "Java Illuminated (5th Ed.)", chapter 2, page 74.
public class Example_02_10__ShortcutArithmeticOperators {
	
	public static void main( String[] args ) {
      
      int a = 5;
      System.out.println("At start, a is: " + a);
      
      a += 10;
      System.out.println("After a += 10, a is: " + a);
      
      a -= 3;
      System.out.println("After a -= 3, a is: " + a);
      
      a *= 2;
      System.out.println("After a *= 2, a is: " + a);
      
      a /= 6;
      System.out.println("After a /= 6, a is: " + a);
      
      a %= 3;
      System.out.println("After a %= 3, a is: " + a);
      
	}
		
}


